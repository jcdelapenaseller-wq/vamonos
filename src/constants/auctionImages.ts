function simpleHash(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  return Math.abs(hash);
}

export const getImageForPropertyType = (type: string | undefined | null, slug: string): string => {
  if (!slug) {
    console.error('getImageForPropertyType called without slug');
    slug = 'fallback-slug';
  }

  let normalizedType = type?.toLowerCase() || 'default';
  
  if (normalizedType.includes('piso') || normalizedType.includes('apartamento') || normalizedType.includes('vivienda') || normalizedType.includes('urbana')) {
    normalizedType = 'piso';
  } else if (normalizedType.includes('casa') || normalizedType.includes('chalet') || normalizedType.includes('unifamiliar') || normalizedType.includes('rustica')) {
    normalizedType = 'casa';
  } else if (normalizedType.includes('local') || normalizedType.includes('nave') || normalizedType.includes('comercial') || normalizedType.includes('oficina')) {
    normalizedType = 'local';
  } else if (normalizedType.includes('garaje') || normalizedType.includes('aparcamiento') || normalizedType.includes('trastero') || normalizedType.includes('plaza')) {
    normalizedType = 'garaje';
  } else {
    normalizedType = 'default';
  }

  const pool = PROPERTY_IMAGES[normalizedType] || PROPERTY_IMAGES.default;
  
  const hash = simpleHash(slug);
  let index = hash % pool.length;
  let selectedImage = pool[index];

  if (!selectedImage) {
    console.error(`Image undefined for type ${normalizedType} and slug ${slug}`);
    selectedImage = PROPERTY_IMAGES.default[0];
  }

  // Ensure 16:9 ratio for Discover
  if (!selectedImage.includes('w=1200') || !selectedImage.includes('h=675')) {
    if (selectedImage.includes('unsplash.com')) {
      try {
        const url = new URL(selectedImage);
        url.searchParams.set('w', '1200');
        url.searchParams.set('h', '675');
        url.searchParams.set('fit', 'crop');
        url.searchParams.set('auto', 'format');
        selectedImage = url.toString();
      } catch (e) {
        console.error('Failed to parse image URL', e);
      }
    }
  }

  return selectedImage;
};
